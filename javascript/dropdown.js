function specialtyDrop() {
  var x = document.getElementById("specialNav");
  if (x.className.indexOf("hiddenDrop") == -1) {
    x.className += " hiddenDrop";
    x.className = x.className.replace("show", "");
  } else {
    x.className += " show";
    x.className = x.className.replace(" hiddenDrop", "");
  }
}

